print(type(15)) # int -> integer -> entero -> número entero
print(type(15.5)) # float -> punto flotante -> número real
print(type(15j)) # complex -> complejo -> número complejo
print(type("15")) # str -> string -> cadena de caracteres
print(type(True)) # bool -> boolean -> booleano -> 
print(type([1,2,4,"hola",True])) # list -> lista -> conjunto ordenado y mutable de datos
print(type((1,2,4,"hola",True))) # tuple -> tupla -> conjunto ordenado e inmutable de datos
print(type({"hola": "mundo", 1: [1,2,3]})) # dict -> diccionario -> conjunto ordenado y nombrado de datos